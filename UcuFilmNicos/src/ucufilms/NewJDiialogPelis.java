public void cargarJList(ILista<Pelicula> peliculas) {
        Pelicula p = (Pelicula) peliculas.getPrimero();
        DefaultListModel dm = new DefaultListModel();
        Pelicula aux = p;
        while (aux != null) {
            dm.addElement(aux.getNombre());
            aux = (Pelicula) aux.getSiguiente();
        }
        jList1.setModel(dm);
        
        
    }
